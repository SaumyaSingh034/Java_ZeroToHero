package javaInterviewPrep;

public class PrintNumberWithoutLoop {

    public static void main(String[] args){
        int number = 'A'/'A';
        int endNumber = 'd';
        System.out.println(endNumber);

        for(int i = number; i<=endNumber;i++){
            System.out.println(i+" ");
        }

        System.out.println("---------------------------------");

    }
}
